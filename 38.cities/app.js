"use strict";
function describe_city(city, country) {
    return ` ${city} is in ${country}`;
}
let city1 = describe_city("karachi", "pakistan");
let city2 = describe_city("hindustan", "india");
let city3 = describe_city("england", "london");
let city4 = describe_city("dubai", "UAE");
console.log(city1);
console.log(city2);
console.log(city3);
console.log(city4);
